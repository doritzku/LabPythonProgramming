class User:
    def __init__(self, name, age, gender, email, height, weight, goal, diet_preference, activity_level):
        self.name = name
        self.age = age
        self.gender = gender
        self.email = email
        self.height = height
        self.weight = weight
        self.goal = goal
        self.diet_preference = diet_preference
        self.activity_level = activity_level
